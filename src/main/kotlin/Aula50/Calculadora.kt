package Aula50

class Calculadora {
    fun somar(a: Double, b: Double): Double {
        return a + b
    }

    fun subtrair(a: Double, b: Double): Double {
        return a - b
    }

    fun multiplicar(a: Double, b: Double): Double {
        return a * b
    }

    fun dividir(a: Double, b: Double): Double {
        return a / b
    }
}